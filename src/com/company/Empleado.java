package com.company;

public class Empleado {

    private String nombre;
    private double sueldo;
    private int dia;
    private int mes;
    private int ano;

    public Empleado (String nombre,double sueldo,int dia,int mes, int ano){

        this.nombre = nombre;
        this.sueldo = sueldo;
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }
}
